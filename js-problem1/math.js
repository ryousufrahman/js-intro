// Math.PI → returns value of π
console.log(Math.PI);

// Math.round() → rounds to nearest integer
console.log(Math.round(4.6));

// Math.floor() → rounds DOWN
console.log(Math.floor(4.9));

// Math.ceil() → rounds UP
console.log(Math.ceil(4.1));

// Math.trunc() → removes decimal part
console.log(Math.trunc(4.9));

// Math.abs() → absolute (positive) value
console.log(Math.abs(-10));

// Math.pow() → power (x^y)
console.log(Math.pow(2, 3));

// Math.sqrt() → square root
console.log(Math.sqrt(25));

// Math.cbrt() → cube root
console.log(Math.cbrt(27));

// Math.max() → largest number
console.log(Math.max(3, 7, 1, 9));

// Math.min() → smallest number
console.log(Math.min(3, 7, 1, 9));

// Math.random() → random number between 0 and 1
console.log(Math.random());

// Random number between 1 and 10
console.log(Math.floor(Math.random() * 10) + 1);

// Math.sign() → returns sign of number
// 1 (positive), -1 (negative), 0
console.log(Math.sign(-5));

// Math.log() → natural log (base e)
console.log(Math.log(10));

// Math.log10() → base 10 logarithm
console.log(Math.log10(100));

// Math.log2() → base 2 logarithm
console.log(Math.log2(8));

// Math.exp() → e power x
console.log(Math.exp(1));

// Math.sin() → sine (radians)
console.log(Math.sin(Math.PI / 2));

// Math.cos() → cosine (radians)
console.log(Math.cos(0));

// Math.tan() → tangent (radians)
console.log(Math.tan(Math.PI / 4));

// Math.asin() → inverse sine
console.log(Math.asin(1));

// Math.acos() → inverse cosine
console.log(Math.acos(1));

// Math.atan() → inverse tangent
console.log(Math.atan(1));

// Math.hypot() → square root of sum of squares
console.log(Math.hypot(3, 4));
